package TimeMachine;

public class Person
{
    //encapsulate instance variables
    private int year;

    //Constructor
    public Person()
    {
        this.year = 1996;
    }

    //Getter

    public int getYear()
    {
        return this.year;
    }

    //Setter

    public void setYear(int year)
    {
        this.year = year;
    }
}
